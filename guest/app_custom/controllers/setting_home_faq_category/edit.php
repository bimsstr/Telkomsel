class Edit extends AdminController
{
    protected $requiredAdminStatus = 'LoggedIn';

    protected function initialize()
    {
        $this->homefaqDataSource = New HomeFaqDataSource($this->mysqli);
    }

    public function index()
    {
        $id = $this->request->getGet('id');

    	$homefaqcategoryDomain = $this->homefaqDataSource->getHomeFaqCategoryDomainById($id);

    	if(!($homefaqcategoryDomain instanceof HomeFaqCategoryDomain))
    	{
    		$sessionMessage['type'] = 'warning';
    		$sessionMessage['message'] = $this->lang->getInternalErrorMessage();
    		$this->session->set('sessionAdminMessage', $sessionMessage);

    		$this->redirect('setting-home-faq');
    		return;
    	}

        if($this->request->getPost('process') == "edit_homefaq_category")
        {
            $this->processEdit($homefaqcategoryDomain);
            return;
        }

        $this->renderEdit($homefaqcategoryDomain);


    }

    private function processEdit(HomeFaqCategoryDomain $homefaqcategoryDomain)
    {
        $data = $this->request->getPost();

        //DATA
        $faq_category = $this->request->getPost('homefaq_category');

        $homefaqcategoryDomain->setCategory($faq_category);

        if($this->homefaqDataSource->update_faqc($homefaqcategoryDomain) == FALSE)
            {

                $sessionAdminMessage['type'] = 'error';
                $sessionAdminMessage['message'] = $this->lang->getInternalErrorMessage();
                $this->session->set('sessionAdminMessage', $sessionAdminMessage);

                $this->redirect('setting-home-faq-category/edit');
                return;
            }

            $sessionAdminMessage['type'] = 'success';
            $sessionAdminMessage['message'] = $this->lang->getInsertSuccessMessage();
            $this->session->set('sessionAdminMessage', $sessionAdminMessage);

            $this->redirect('setting-home-faq');
            return;
    }

